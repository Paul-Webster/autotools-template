#include <math.h>

/*=============================================================================
 * int dmf_add(int v1, int v2)
 *============================================================================*/
int dmf_add(int v1, int v2) {
	__asm__ __volatile__ (
			"addl %%ebx, %%eax"
			:"=a"(v1)
			:"a"(v1), "b"(v2)
	);
	return v1;
}

